     public void llenarComboMetodo(){
        comboMetodoPago.removeAllItems();
        comboMetodoPago.addItem("Seleccione...");
        for (int i = 0; i < metodo.length; i++) {
            comboMetodoPago.addItem(metodo[i]);
            
        }
    }
      public void  buscarNombre(){
          try { 
            Connection db = dbConection.conectar();

            CallableStatement cs = db.prepareCall("CALL ObtenerNombreUsuario(?, ?)");
            cs.setInt(1, idU);  
            cs.registerOutParameter(2, Types.VARCHAR);  

            cs.execute();

            String nombreUsuario = cs.getString(2);  
            nombre=nombreUsuario;  

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    
    }

    public void llenarComboTipo() {
    comboTipoVehiculo.removeAllItems();
    comboTipoVehiculo.addItem("Seleccione...");

    try {
        Connection db = dbConection.conectar();
        CallableStatement cs = db.prepareCall("CALL ObtenerTiposVehiculo()");
        ResultSet rs = cs.executeQuery();

        while (rs.next()) {
            comboTipoVehiculo.addItem(rs.getString("tipoVehiculo"));
        }

        rs.close();
        cs.close();
        db.close();

    } catch (SQLException ex) {
        ex.printStackTrace();
    }
}
    
     public void guardar(){
        
        llenar();
         if(    placa != null && !placa.isEmpty() &&
                tipoVhiculo != null && !tipoVhiculo.isEmpty() &&
                metodoPago != null && !metodoPago.isEmpty())               
               {

                  in.igresarVehiculo(placa, tipoVhiculo);
                  in.transacciones(placa,tarifa, metodoPago, totalPago,nombre,null);
                  
                  txtPlaca.setText("");
                  llenarComboTipo();
                 llenarComboMetodo();
           
        }else{
             
              JOptionPane.showMessageDialog(rootPane, "faltan campos por llenar");
            
        }
        
        
              
    
    }

private String validarLongitud(JTextField campo, int maxLongitud, String mensajeError) {
    String texto = campo.getText();
    if (texto.length() <= maxLongitud) {
        return texto.toUpperCase();
    } else {
        JOptionPane.showMessageDialog(rootPane, mensajeError);
        return "";
    }
}

    
    public void llenar() {
    placa = validarLongitud(txtPlaca, 6, "La placa ha superado el lÃ­mite...");
    tipoVhiculo = comboTipoVehiculo.getSelectedIndex() > 0 ? comboTipoVehiculo.getSelectedItem().toString() : null;
    metodoPago = comboMetodoPago.getSelectedIndex() > 0 ? metodo[comboMetodoPago.getSelectedIndex() - 1] : null;
    tarifa = 0;
    totalPago = 0;

    if (tipoVhiculo != null) {
        try {
            Connection db = dbConection.conectar();
            CallableStatement cs = db.prepareCall("CALL ObtenerTarifaPorTipo(?, ?)");

            cs.setString(1, tipoVhiculo);
            cs.registerOutParameter(2, Types.INTEGER); 

            cs.execute();

            tarifa = cs.getInt(2); 

            cs.close();
            db.close();

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
}
    
  
    
    

    
